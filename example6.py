# Create a turtle object
my_turtle = turtle.Turtle()

my_turtle.width(3)
# Turn off animation
# turtle.tracer(0)

# Set the speed of the turtle (0 is the fastest, 1 is slow, 10 is normal)
my_turtle.speed(2)

# Set the starting position
my_turtle.penup()
my_turtle.goto(-200, 0)
my_turtle.pendown()

# Write the word "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
word = "SHAYAN"
for letter in word:
    draw_letter(my_turtle, letter, size=100)
    my_turtle.penup()
    my_turtle.forward(40)  # Add spacing between letters
    my_turtle.pendown()

# Hide the turtle
my_turtle.hideturtle()

# Update the screen to show the final result
turtle.update()

# Display the window
turtle.done()
